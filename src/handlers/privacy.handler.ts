import type { MotobroContext } from "../types/bot.js";

export async function handlePrivacy(ctx: MotobroContext): Promise<void> {
  await ctx.reply(
    [
      "Конфиденциальность:",
      "• Полный маршрут не хранится — только последняя точка и примерный район.",
      "• Точные координаты в группе по умолчанию не показываются.",
      "• Данные нужны, чтобы понять «кто катается» и примерное место, без постоянного трекинга.",
      "• Остановить поездку можно через /stop; в настройках можно удалить сохранённую последнюю точку.",
    ].join("\n"),
  );
}
